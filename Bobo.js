/* SOLID principles
        = a set of design principles intended to improve software design and maintainability.
        helps create software easy to manage, extend and understand.
1) Single Responsibility Principle
        = a class should have only one job or responsibility
        = this reduces complexity of classes 
2) Open / Closed Principle
        = software entities (classes, modules, functions) should be open for extension but
        closed for modification
        = reduced risk of introducing bugs
3) Liskov Substitution Principle
        = sybtypes must be substitutable for their base types without altering how the program works
4) Interface Segragation
        = clients should not be forced to depend on interfaces they do not use
        = creates smaller, specific interfaces 
5) Dependency Inversion
        = high-level modules should not depend on low-level modules
        = abstractions should not depend on details, but details on abstractions
        = decoupling

OOP == Object-Oriented Programming
 - 4 principles : Encapsulation , Abstraction , Inheritance , Polymorphism
        ENCAPSULATION : - bundling data and methods into a single unit (called class)
                        - prevents data from being modified this way
        ABSTRACTION : - hiding complex implementation details and showing only the necessary features
                      of an object.
                      - simplifies the interaction with complex systems by providing a simplified
                        interface; increased efficiency; reduces complexity
        INHERITANCE : - mechanism by which one class can inherit properties and methods from another
                        class. Reusability
                      - allows for the extension of functionality without duplication
        POLYMORPHISM : - allows objects of different classes to be terated as objects of a common
                        superclass. enables a single interface to controll access to a general class
                        of actions
                       - provides flexibility in programming and can handle different types of data

IDEMPOTENT HTTP
- method that can be called multiple times without changing the result beyond the initial pplication;
making the same request multiple times will have the same effect as making it once
- GET , PUT , DELETE , HEAD , OPTIONS

*/